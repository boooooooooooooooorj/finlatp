    private void regbttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regbttnActionPerformed
        String name = nameField.getText();
        String uname = unameField.getText();
        String password = passField.getText();
        
        try{
                      
            FileOutputStream file = new FileOutputStream("Reg.txt", true);
            PrintWriter printer = new PrintWriter(file);
            
            printer.println(name + "," + uname + "," + password);
            JOptionPane.showMessageDialog(null, "REGISTERED SUCCESSFULLY");
            printer.close();
                     
            
            Log_In log = new Log_In();
            log.setVisible(true);
            this.dispose();
            
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "REGISTRATION UNSUCCESSFULL.");
            
        }       
        nameField.setText("");
        unameField.setText("");
        passField.setText("");
    }//GEN-LAST:event_regbttnActionPerformed

